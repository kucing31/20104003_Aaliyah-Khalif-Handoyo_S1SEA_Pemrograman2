package Modul7.percobaan.p3;

public class Mahasiswa {
    public void info(){
        System.out.println("Haloo... Aku Mahasiswa!!");
    }

    public void info(String nama){
        System.out.println("Haloo... Namaku " + nama);
    }

    /*
    ciri-ciri overloading: dalam sebuah class harus
    1. Nama method harus sama
    2. Daftar parameter harus berbeda
    3. Return type boleh sama, juga boleh berbeda
    */
}
